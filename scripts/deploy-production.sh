# 4. –°–æ–∑–¥–∞–Ω–∏–µ –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–æ–≥–æ –±—ç–∫–∞–ø–∞
info "–°–æ–∑–¥–∞–Ω–∏–µ –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–æ–≥–æ –±—ç–∫–∞–ø–∞ –ë–î..."
BACKUP_DIR="./data/backups"
mkdir -p "$BACKUP_DIR"
TIMESTAMP=$(date +"%Y%m%d_%H%M%S")
BACKUP_FILE="$BACKUP_DIR/pre_deploy_${TIMESTAMP}.db"

if [ -f "./data/coworking.db" ]; then
    cp ./data/coworking.db "$BACKUP_FILE"
    success "–ë—ç–∫–∞–ø —Å–æ–∑–¥–∞–Ω: $BACKUP_FILE"
else
    warning "–ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –Ω–µ –Ω–∞–π–¥–µ–Ω–∞, –±—ç–∫–∞–ø –ø—Ä–æ–ø—É—â–µ–Ω"
fi

# 5. –ü—Ä–æ–≤–µ—Ä–∫–∞ Python –Ω–∞ —Ö–æ—Å—Ç–µ
info "–ü—Ä–æ–≤–µ—Ä–∫–∞ Python –Ω–∞ —Ö–æ—Å—Ç–µ..."
if ! command -v python3 &> /dev/null; then
    error "Python3 –Ω–µ –Ω–∞–π–¥–µ–Ω –Ω–∞ —Ö–æ—Å—Ç–µ!"
    error "–£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ Python3: apt-get install python3 python3-pip python3-venv"
    exit 1
fi

PYTHON_VERSION=$(python3 --version)
info "–ù–∞–π–¥–µ–Ω: $PYTHON_VERSION"

# 6. –ü—Ä–æ–≤–µ—Ä–∫–∞/—Å–æ–∑–¥–∞–Ω–∏–µ –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–≥–æ –æ–∫—Ä—É–∂–µ–Ω–∏—è
if [ ! -d "venv" ]; then
    info "–°–æ–∑–¥–∞–Ω–∏–µ –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–≥–æ –æ–∫—Ä—É–∂–µ–Ω–∏—è..."
    python3 -m venv venv || {
        error "–ù–µ —É–¥–∞–ª–æ—Å—å —Å–æ–∑–¥–∞—Ç—å venv"
        exit 1
    }
    success "–í–∏—Ä—Ç—É–∞–ª—å–Ω–æ–µ –æ–∫—Ä—É–∂–µ–Ω–∏–µ —Å–æ–∑–¥–∞–Ω–æ"
fi

# 7. –ê–∫—Ç–∏–≤–∞—Ü–∏—è venv –∏ —É—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π
info "–ê–∫—Ç–∏–≤–∞—Ü–∏—è –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–≥–æ –æ–∫—Ä—É–∂–µ–Ω–∏—è..."
source venv/bin/activate || {
    error "–ù–µ —É–¥–∞–ª–æ—Å—å –∞–∫—Ç–∏–≤–∏—Ä–æ–≤–∞—Ç—å venv"
    exit 1
}

info "–£—Å—Ç–∞–Ω–æ–≤–∫–∞/–æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π..."
pip install --quiet -r requirements.txt || {
    error "–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏"
    exit 1
}
success "–ó–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã"

# 8. –ó–∞–ø—É—Å–∫ –º–∏–≥—Ä–∞—Ü–∏–π –ù–ê –•–û–°–¢–ï
info "–ó–∞–ø—É—Å–∫ –º–∏–≥—Ä–∞—Ü–∏–π –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö –ù–ê –•–û–°–¢–ï..."
echo ""

python3 migrations/run_all_migrations.py --auto-confirm

if [ $? -ne 0 ]; then
    error "‚ùå –ú–∏–≥—Ä–∞—Ü–∏–∏ –Ω–µ —É–¥–∞–ª–∏—Å—å!"
    warning "–í–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏–µ –∏–∑ –±—ç–∫–∞–ø–∞..."

    if [ -f "$BACKUP_FILE" ]; then
        cp "$BACKUP_FILE" ./data/coworking.db
        success "–ë–î –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞ –∏–∑ –±—ç–∫–∞–ø–∞"
    fi

    error "–î–µ–ø–ª–æ–π –ø—Ä–µ—Ä–≤–∞–Ω. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –ª–æ–≥–∏ –º–∏–≥—Ä–∞—Ü–∏–π."
    exit 1
fi

echo ""
success "–ú–∏–≥—Ä–∞—Ü–∏–∏ –∑–∞–≤–µ—Ä—à–µ–Ω—ã —É—Å–ø–µ—à–Ω–æ"

# 9. –î–µ–∞–∫—Ç–∏–≤–∞—Ü–∏—è venv
deactivate

# 10. –ü–µ—Ä–µ—Å–±–æ—Ä–∫–∞ Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤
info "–ü–µ—Ä–µ—Å–±–æ—Ä–∫–∞ Docker –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤..."
docker-compose build || {
    error "–û—à–∏–±–∫–∞ —Å–±–æ—Ä–∫–∏ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤"
    exit 1
}
success "–ö–æ–Ω—Ç–µ–π–Ω–µ—Ä—ã —Å–æ–±—Ä–∞–Ω—ã"

# 11. –ó–∞–ø—É—Å–∫ —Å–µ—Ä–≤–∏—Å–æ–≤
info "–ó–∞–ø—É—Å–∫ —Å–µ—Ä–≤–∏—Å–æ–≤..."
docker-compose up -d || {
    error "–û—à–∏–±–∫–∞ –∑–∞–ø—É—Å–∫–∞ —Å–µ—Ä–≤–∏—Å–æ–≤"
    exit 1
}
success "–°–µ—Ä–≤–∏—Å—ã –∑–∞–ø—É—â–µ–Ω—ã"

# 12. –û–∂–∏–¥–∞–Ω–∏–µ –≥–æ—Ç–æ–≤–Ω–æ—Å—Ç–∏
