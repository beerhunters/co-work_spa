#!/bin/bash
#
# –°–∫—Ä–∏–ø—Ç –¥–µ–ø–ª–æ—è –Ω–∞ Production
# –ó–∞–ø—É—Å–∫–∞–µ—Ç –º–∏–≥—Ä–∞—Ü–∏–∏ –ù–ê –•–û–°–¢–ï, –∑–∞—Ç–µ–º –ø–æ–¥–Ω–∏–º–∞–µ—Ç Docker
#
# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./scripts/deploy-production.sh
#

set -e  # –û—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø—Ä–∏ –æ—à–∏–±–∫–µ

# –¶–≤–µ—Ç–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞
